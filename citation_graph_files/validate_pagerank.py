def analyze_pagerank_stability(graph_path, landmark_ids):
    """
    Analyzes the stability of PageRank rankings for a core set of papers
    by comparing their ranks in a core graph vs. the full graph.
    """
    logging.info(f"Loading full graph from {graph_path}...")
    try:
        G_full = nx.read_graphml(graph_path)
    except FileNotFoundError:
        logging.error(f"File not found: {graph_path}.")
        return

    # --- Step 1: Create the "Core" Graph ---
    # Find which of our landmark IDs are actually in the full graph
    core_nodes_in_graph = [node for node in landmark_ids if node in G_full]
    if not core_nodes_in_graph:
        logging.error("None of the specified landmark paper IDs were found in the graph.")
        return
        
    # Create a subgraph containing only these core nodes and their connections
    G_core = G_full.subgraph(core_nodes_in_graph)
    logging.info(f"Created core graph with {G_core.number_of_nodes()} nodes and {G_core.number_of_edges()} edges.")

    # --- Step 2 & 4: Calculate PageRank for Both Graphs ---
    logging.info("Calculating PageRank for both core and full graphs...")
    pagerank_core = nx.pagerank(G_core, weight='weight')
    pagerank_full = nx.pagerank(G_full, weight='weight')

    # --- Step 3 & 5: Create Ranked DataFrames ---
    # Create a DataFrame for the core graph results
    df_core = pd.DataFrame(pagerank_core.items(), columns=['paperId', 'pagerank_core'])
    df_core['rank_core'] = df_core['pagerank_core'].rank(ascending=False)
    
    # Create a DataFrame for the full graph results, but ONLY for the core papers
    core_pagerank_in_full = {pid: score for pid, score in pagerank_full.items() if pid in core_nodes_in_graph}
    df_full = pd.DataFrame(core_pagerank_in_full.items(), columns=['paperId', 'pagerank_full'])
    df_full['rank_full'] = df_full['pagerank_full'].rank(ascending=False)

    # --- Step 6: Compare the Rankings ---
    # Merge the two DataFrames on the paperId to align the ranks
    merged_df = pd.merge(df_core, df_full, on='paperId')
    
    # Calculate Kendall's Tau rank correlation
    tau, p_value = kendalltau(merged_df['rank_core'], merged_df['rank_full'])
    
    print("\n" + "="*80)
    print("  PageRank Stability Analysis Summary")
    print("="*80)
    print(f"Kendall's Tau Rank Correlation: {tau:.4f}")
    print(f"P-value: {p_value:.4f}")
    print("\nInterpretation:")
    if tau > 0.8:
        print(" -> Excellent Stability: The relative importance of landmark papers is highly consistent.")
    elif tau > 0.6:
        print(" -> Good Stability: There is a strong correlation in the rankings.")
    else:
        print(" -> Lower Stability: The addition of minor papers significantly altered the rankings of the core hub.")
    print("="*80)
    print(len(lp))
    
